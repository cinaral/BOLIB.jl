function TollSettingP3()
	n1::Int64 = 3
	n2::Int64 = 18

	function F(xy)
		x = @view xy[1:n1]
		y = @view xy[n1+1:n1+n2]
		- x[1]*(y[1] + 10*y[2]) - x[2]*(y[3] + 10*y[4]) - x[3]*(y[5] + 10*y[6])
	end

	function G(xy)
		x = @view xy[1:n1]
		y = @view xy[n1+1:n1+n2]
		[x[1]; x[2]; x[3]]
	end

	function f(xy)
		x = @view xy[1:n1]
		y = @view xy[n1+1:n1+n2]
		5*y[7] + 7*y[8] + 14*y[9] + 7*y[10] + 2*y[11] + 4*y[12] + 29*y[13] + 20*y[14] + 12*y[15] + 8*y[16] + 5*y[17] + 2*y[18] + 2*x[1]*y[1] + 20*x[1]*y[2] + 2*x[2]*y[3] + 20*x[2]*y[4] + 2*x[3]*y[5] + 20*x[3]*y[6]
	end

	function g(xy)
		x = @view xy[1:n1]
		y = @view xy[n1+1:n1+n2]
		[1 - y[8] - y[9] - y[7]; 1 - y[11] - y[12] - y[10]; 1 - y[14] - y[15] - y[13]; 1 - y[17] - y[18] - y[16]; y[7] - y[5] - y[1] - y[13]; y[10] - 10*y[6] - 10*y[2] - y[16]; y[1] - y[3] + y[8] - y[14]; 10*y[2] - 10*y[4] + y[11] - y[17]; y[3] + y[5] + y[9] - y[15]; 10*y[4] + 10*y[6] + y[12] - y[18]; y[7] + y[8] + y[9] - 1; y[10] + y[11] + y[12] - 1; y[13] + y[14] + y[15] - 1; y[16] + y[17] + y[18] - 1; y[1] + y[5] - y[7] + y[13]; 10*y[2] + 10*y[6] - y[10] + y[16]; y[3] - y[1] - y[8] + y[14]; 10*y[4] - 10*y[2] - y[11] + y[17]; y[15] - y[5] - y[9] - y[3]; y[18] - 10*y[6] - y[12] - 10*y[4]; y[1]; y[2]; y[3]; y[4]; y[5]; y[6]; y[7]; y[8]; y[9]; y[10]; y[11]; y[12]; y[13]; y[14]; y[15]; y[16]; y[17]; y[18]]
	end

	xy_init = Float64[0;0;0;0;0;0;10;1;0;1;0;0;0;10;0;0;0;1;0;0;10]
	Ff_optimal = Float64[-3.5;32;2]

	(; n1, n2, F, G, f, g, xy_init, Ff_optimal)
end 

